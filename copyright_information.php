        <h2 class="header">Distributing Copyrighted Materials through CCLE</h2>
        <p>All materials distributed through the CCLE system must be in compliance with <a href="http://www.copyright.gov/title17/">Title 17</a> of the US Code, generally known as the <a href="http://www.copyright.gov/title17/">1976 Copyright Law</a>.</p>
        <p>Every reproduction of a copyrighted work (which includes the act of uploading materials to CCLE) should be done in compliance with this law, and it is the user's responsibility to determine compliance.  The instructor of record is responsible for all materials uploaded by teaching assistants, staff, or others doing so at the instructor's request.</p>
        <p>All users of CCLE should become familiar with <a href="http://www.universityofcalifornia.edu/copyright/systemwide/ucpolicies.html">UC Systemwide Copyright Policies and Resources</a>, and can find additional resources and local information at the <a href="http://www.library.ucla.edu/copyright/">UCLA Library Copyright Page</a>.  Within these pages you'll find lots of helpful tools and guides, on topics such as:</p>
        <ul>
            <li><a href="http://www.universityofcalifornia.edu/copyright/permission.html">Obtaining Permissions</a> - a step-by-step guide with strategies for getting permission to use copyrighted works for educational purposes.</li>
            <li><a href="http://www.universityofcalifornia.edu/copyright/publicdomain.html">Public Domain</a> - works that are not covered by copyright protection.</li>
            <li><a href="http://www.universityofcalifornia.edu/copyright/fairuse.html">Fair Use</a> - factors to help determine whether your use qualifies as &quot;fair use.&quot;</li>
            <li><a href="http://www.universityofcalifornia.edu/copyright/newmedia.html">Web Sites</a> - copyright law also applies to electronic resources, including web sites.</li>
            <li><a href="http://www.universityofcalifornia.edu/copyright/teach.html">The TEACH Act</a> - establishes new standards for copyright and distance education</li>
        </ul>
        <h2 class="header">Copyright Ownership of Course Materials</h2>
        <p>Though there are important exceptions included within the <a href="http://www.universityofcalifornia.edu/copyright/systemwide/pocmdi.html">2003 Policy on Ownership of Course Materials</a>, generally &quot;ownership of the rights to Course Materials, including copyright, shall reside with the Designated Instructional Appointee who creates them.&quot;  Anyone wishing to copy or redistribute materials found on CCLE should secure permission from the copyright owner before doing so.</p>
